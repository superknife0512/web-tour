const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const adminSchema = new Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  resetCode: {
    type: String,
    default: ''
  },
  expire: {
    type: Date,
    default: ''
  },
  role: {
    type: String,
    required: true
  }
});

module.exports = mongoose.model('admins', adminSchema)